public class Cadastro  {

	private float cpf;
	private String nome;

	Cadastro() {
		this.cpf = 0;
		this.nome = "";
	}

	public float getCpf() {
		return cpf;
	}

	public void setCpf(float cpf) {
		this.cpf = cpf;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}



}
